            gpsUsed = 0,
            glonassVisible = 0,
            glonassUsed = 0,
            galileoVisible = 0,
            galileoUsed = 0,
            beidouVisible = 0,
            beidouUsed = 0,
            qzssVisible = 0,
            qzssUsed = 0,
            sbasVisible = 0,
            sbasUsed = 0,
            rawGpsCount = 0,
            rawGlonassCount = 0,
            rawGalileoCount = 0,
            rawBeidouCount = 0,
            rawQzssCount = 0,
