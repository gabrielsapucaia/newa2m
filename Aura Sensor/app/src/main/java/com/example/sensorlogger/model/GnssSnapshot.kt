            elapsedRealtimeNanos = 0L,
            satellitesVisible = 0,
            satellitesUsed = 0,
            cn0Average = 0f,
            cn0Min = null,
            cn0Max = null,
            cn0Percentile25 = null,
            cn0Median = null,
            cn0Percentile75 = null,
